public class Ternary {
    public static void main(String[] args) {
        java.lang.Integer.valueOf("80");
        System.out.println(java.lang.Integer.valueOf(((java.lang.String)"field_originatorName.getValue()").length()) > java.lang.Integer.valueOf(80));
        System.out.println(java.lang.Integer.valueOf("$F{originatorName}".length()) > java.lang.Integer.valueOf(80));
        System.out.println("$F{originatorName}".length() > 10 ? "$F{originatorName}".substring(0,10) : "$F{originatorName}");
    }
}
